    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int x = jTable1.getRowCount();
       List<Integer> b = new ArrayList<Integer>();       
       
       for (int i=0;i<x;i++){
           if ( jTable1.getModel().getValueAt(i, 6).equals(true)){
               b.add((Integer) jTable1.getModel().getValueAt(i, 7));
           }
       }     
      
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Opravdu vydat "+b.size()+" ks materiálu?", "Prosím potvrďte", dialogButton);
        if(dialogResult == 0) {     
       
      String sql = "UPDATE APP.SKLAD SET VYDEJKA=?, VYDANI=? WHERE ID=?"; 
        try {
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(this.textField1.getText().toString())+1);
            
            Calendar cal = Calendar.getInstance();
            java.util.Date d =  cal.getTime();
            ps.setDate(2, this.konvertuj(d) );
            for (int i=0;i<b.size();i++){
                ps.setInt(3, b.get(i));
                ps.executeUpdate();
            }
            ps.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.naplntabulku();
        this.zjistivydejku();
         } 
   
        
    }//GEN-LAST:event_jButton2ActionPerformed
