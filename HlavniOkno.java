    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        Calendar cal = Calendar.getInstance();
        int rok = cal.get(Calendar.YEAR);
        java.sql.Date ted = konvertuj(cal.getTime());
        cal.clear();
        cal.set(rok, 0, 1);
        java.sql.Date prvniden = konvertuj(cal.getTime());
        
        PDFdocument dokument = new PDFdocument();
        dokument.opendoc();
        try {
            dokument.hlavicka();
            dokument.vydejka(("Výdejka číslo "+this.textField1.getText()+"/"),this.datumvydejky(Integer.parseInt(this.textField1.getText())));
            dokument.adresa(this.getadr(this.VybranaFirma));

            String sql = "Select NAZEV,SN,DL,POCET,VYDANI FROM APP.SKLAD WHERE FIRMA=? AND KATEGORIE=? AND VYDEJKA=? AND VYDANI between ? and ? ORDER BY NAZEV";
            PreparedStatement ps = conn.prepareStatement(sql,ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ps.setInt(1, this.VybranaFirma);
            ps.setInt(3, Integer.parseInt(this.textField1.getText()));
            ps.setDate(4, prvniden);
            ps.setDate(5, ted);
            for (int i=1;i<4;i++){
                ps.setInt(2, i);
                ResultSet rs = ps.executeQuery();
                switch (i) {
                    case 1:
                        dokument.datatabulky("PM/ICD", rs);
                        break;
                    case 2:
                        dokument.datatabulky("Elektrody", rs);
                        break;    
                    case 3:                       
                        dokument.datatabulky("Příslušenství", rs);
                        break;                         
                } rs.close(); }   ps.close();
            dokument.celkove();
            dokument.closedoc();   
        } catch (DocumentException | SQLException ex) {
            Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(this, "Soubor uložen");

    }//GEN-LAST:event_jButton3ActionPerformed
