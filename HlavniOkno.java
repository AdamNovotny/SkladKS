    private void naplntabulku() { 
     int columns = 7;
     String sql = "select * from APP.nasklade WHERE FIRMA=?";
     while (jTable1.getRowCount() > 0) {
                ((DefaultTableModel) jTable1.getModel()).removeRow(0);
            }      
     try {
          PreparedStatement ps = conn.prepareStatement(sql);  
          ps.setInt(1, this.VybranaFirma);
          ResultSet rs = ps.executeQuery();
          int pom = 0;
            
          while (rs.next() ) {
              
              Object[] row = new Object[columns+1];
              
              for (int i = 1; i <= columns; i++)
                {
                  if (i==columns){
                      row[i-1] = Boolean.FALSE;
                      row[i] = rs.getObject(i+1); 
                  } else {
                      
                   if (rs.getObject(i+1) instanceof java.sql.Date ) {  
                    row[i - 1] = konvertuj2((java.sql.Date)rs.getObject(i+1)); } else {
                    row[i-1] = rs.getObject(i+1); 
                   }
                   
                }
                }          
              
              ((DefaultTableModel) jTable1.getModel()).insertRow(pom,row);
               pom++;             
          }  
            
        ps.close();
        rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(HlavniOkno.class.getName()).log(Level.SEVERE, null, ex);
        }    
     TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(jTable1.getModel());
     jTable1.setRowSorter(sorter);
     jTable1.updateUI();
        
    }
